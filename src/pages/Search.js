  async function connect() {

    const provider = new ethers.providers.Web3Provider(window.ethereum, "any");
    // Prompt user for account connections
    await provider.send("eth_requestAccounts", []);
    const signer = provider.getSigner();
    console.log("Account:", await signer.getAddress());

   // let web3 = await getWeb3();

    let accounts = await provider.send("eth_requestAccounts", []);;

    let godonor = new ethers.Contract(
      "0xe7f1725E7734CE288F8367e1Bb143E90bb3F0512",
      godonorABI,
      provider
    );
    let donor = new ethers.Contract(
      "0x5FbDB2315678afecb367f032d93F642f64180aa3",
      donorABI,
      provider
    )
    let totalFundCount = await godonor.onGoingFunds();
    let arr = [];
    
     for(let i = 1; i <= totalFundCount; i++) {
      let fund = await godonor.getById(i);
      let info = await godonor.balanceRaised(fund);
      let a = info.id.toString();
      let b = info.amountRaised.toString();
      let c = info.targetAmount.toString();
      let d = info.startBlock.toString();
      let e = info.endBlock.toString();
      let x = [a,b,c,d,e];
      arr.push(x);
     }

    
    setFundraisers(arr);
    setTotalFundCount(totalFundCount);
    console.log(arr);
    setAccounts(accounts);
   // setWeb3(web3);
    setGodonor(godonor);
    setDonor(donor);
    setProvider(provider);
  }

